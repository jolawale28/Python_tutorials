pi = 3.1415926535897932

# Print out the value of PI to a whole number
to_a_whole_number = round(pi)
to_1_decimal_place = round(pi, 1)
to_2_decimal_places = round(pi, 2)
to_3_decimal_places = round(pi, 3)

print(f"Whole number: {to_a_whole_number}")
print(f"1 dp: {to_1_decimal_place}")
print(f"2 dp: {to_2_decimal_places}")
print(f"3 dp: {to_3_decimal_places}")